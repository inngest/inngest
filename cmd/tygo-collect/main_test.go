func TestCollectAnnotatedTypes(t *testing.T) {
	tests := []struct {
		name      string
		files     map[string]string // filename -> content
		wantNames []string
		wantErr   string
	}{
		{
			name: "single type with annotation",
			files: map[string]string{
				"types.go": `package test

//tygo:generate
type MyStruct struct {
	Field string
}
`,
			},
			wantNames: []string{"MyStruct"},
		},
		{
			name: "const block with annotation",
			files: map[string]string{
				"consts.go": `package test

//tygo:generate
const (
	MyConst = "value"
)
`,
			},
			wantNames: []string{"MyConst"},
		},
		{
			name: "type without annotation excluded",
			files: map[string]string{
				"types.go": `package test

type NoAnnotation struct {
	Field string
}

//tygo:generate
type WithAnnotation struct {
	Field string
}
`,
			},
			wantNames: []string{"WithAnnotation"},
		},
		{
			name: "annotation with blank line still detected",
			files: map[string]string{
				"types.go": `package test

//tygo:generate

type MyStruct struct {
	Field string
}
`,
			},
			wantNames: []string{"MyStruct"},
		},
		{
			name: "multiple types in same file",
			files: map[string]string{
				"types.go": `package test

//tygo:generate
type First struct {}

//tygo:generate
type Second struct {}

type Third struct {} // no annotation
`,
			},
			wantNames: []string{"First", "Second"},
		},
		{
			name: "annotation blocked by other comment",
			files: map[string]string{
				"types.go": `package test

//tygo:generate
// This is a doc comment
type MyStruct struct {}
`,
			},
			wantNames: []string{}, // Should not be collected because doc comment is between annotation and type
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			// Create temp directory with test files
			tmpDir := t.TempDir()
			for filename, content := range tt.files {
				path := filepath.Join(tmpDir, filename)
				err := os.WriteFile(path, []byte(content), 0644)
				require.NoError(t, err, "failed to write test file")
			}

			collected, err := collectAnnotatedTypes([]string{tmpDir})

			if tt.wantErr != "" {
				require.Error(t, err)
				require.Contains(t, err.Error(), tt.wantErr)
				return
			}

			require.NoError(t, err)
			require.Len(t, collected, len(tt.wantNames))

			gotNames := make(map[string]bool)
			for _, c := range collected {
				gotNames[c.Name] = true
			}

			for _, want := range tt.wantNames {
				require.True(t, gotNames[want], "expected type %q not found in collected types", want)
			}
		})
	}
}

func TestDuplicateDetection(t *testing.T) {
	// Create two temp directories (simulating two packages) with same type name
	tmpDir1 := t.TempDir()
	tmpDir2 := t.TempDir()

	file1 := `package pkg1

//tygo:generate
type DuplicateName struct {}
`
	file2 := `package pkg2

//tygo:generate
type DuplicateName struct {}
`

	err := os.WriteFile(filepath.Join(tmpDir1, "types.go"), []byte(file1), 0644)
	require.NoError(t, err, "failed to write test file")

	err = os.WriteFile(filepath.Join(tmpDir2, "types.go"), []byte(file2), 0644)
	require.NoError(t, err, "failed to write test file")

	_, err = collectAnnotatedTypes([]string{tmpDir1, tmpDir2})
	require.Error(t, err, "expected duplicate detection error")
	require.Contains(t, err.Error(), "duplicate type")
}

func TestWriteBarrelFile(t *testing.T) {
	tmpDir := t.TempDir()
	output := filepath.Join(tmpDir, "subdir", "output.go")

	collected := []collectedType{
		{Name: "MyType", Source: "type MyType struct {}", SourceFile: "source.go"},
	}

	err := writeBarrelFile(output, "mypkg", collected)
	require.NoError(t, err)

	content, err := os.ReadFile(output)
	require.NoError(t, err, "failed to read output file")

	contentStr := string(content)

	require.Contains(t, contentStr, "package mypkg", "output should contain correct package name")
	require.Contains(t, contentStr, "Code generated by tygo-collect", "output should contain generated header")
	require.Contains(t, contentStr, "// From source.go", "output should contain source file comment")
	require.Contains(t, contentStr, "type MyType struct {}", "output should contain the type definition")
}

func TestConstTypeAnnotationStripped(t *testing.T) {
	tmpDir := t.TempDir()

	// Create a file with a typed const
	file := `package test

//tygo:generate
const (
	MyConst SomeType = "value"
)
`
	err := os.WriteFile(filepath.Join(tmpDir, "consts.go"), []byte(file), 0644)
	require.NoError(t, err, "failed to write test file")

	collected, err := collectAnnotatedTypes([]string{tmpDir})
	require.NoError(t, err)
	require.Len(t, collected, 1)

	// The type annotation should be stripped from the source
	require.NotContains(t, collected[0].Source, "SomeType", "const type annotation should be stripped from output")
}

func TestHasAnnotation(t *testing.T) {
	tests := []struct {
		name       string
		commentMap map[int]string
		declLine   int
		want       bool
	}{
		{
			name:       "annotation immediately before",
			commentMap: map[int]string{9: "//tygo:generate"},
			declLine:   10,
			want:       true,
		},
		{
			name:       "annotation with one blank line",
			commentMap: map[int]string{8: "//tygo:generate"},
			declLine:   10,
			want:       true,
		},
		{
			name:       "annotation with two blank lines",
			commentMap: map[int]string{7: "//tygo:generate"},
			declLine:   10,
			want:       true,
		},
		{
			name:       "annotation too far away",
			commentMap: map[int]string{6: "//tygo:generate"},
			declLine:   10,
			want:       false,
		},
		{
			name:       "different comment blocks annotation",
			commentMap: map[int]string{8: "//tygo:generate", 9: "// doc comment"},
			declLine:   10,
			want:       false,
		},
		{
			name:       "no annotation",
			commentMap: map[int]string{9: "// just a comment"},
			declLine:   10,
			want:       false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := hasAnnotation(tt.commentMap, tt.declLine)
			require.Equal(t, tt.want, got)
		})
	}
}
